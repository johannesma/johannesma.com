	    <hgroup class="dt">
	    	<h2>What's a BadJo?</h2>
	    	<h4>A quick anecdote</h4>
	    </hgroup>
	    <p class="dt">A couple years ago, my friend Katyana gave me this little card. It had a pencil-drawn, scrawny, big-headed ninja on it and it said "BADJO". One word, no spaces, how perfect. The best part about this card is that it was drawn by her little sister who meant to draw it for her dad, Joe. When she misspelled the name, Katyana asked if she could give it to me instead, and that was how BadJo was created. </p>
		
	   	<h3>Objective</h3>
	 
		<p>I want my work to tell a story. Through solid foundations and an active imagination, I want to keep exploring the relationship between design and technology. I appreciate old-fashioned aesthetics and the basic mechanics of things: the look of wood grain, the durability of things made with solid wood and metal instead of plastic, how bicycles, motorcycles, and analog electronics work. There is a more personal relationship with these technologies because of our physical relationship with them. 
		<p>I would like to translate that intimate experience to what I create for the interactive world. I want to apply my knowledge of how those things were built, how they function, and how they shape human life as a basis of the next generation of technology. Technology that's functional, sustainable, and delightful. Most of all, I'd like to have fun doing it. Let me create with you.</p>
